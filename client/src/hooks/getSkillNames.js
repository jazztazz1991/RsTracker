const sorting = (a, b) => {
    if (a.id < b.id) {
        return -1;
    }
    if (a.id > b.id) {
        return 1;
    }
    return 0;
}

const addNames = (skills) => {
    skills.forEach((skill) => {
        skill.name = skillNames[skill.id]
    })
    skills.sort(sorting)
    console.log(skills)
    return skills;
}

export default addNames;